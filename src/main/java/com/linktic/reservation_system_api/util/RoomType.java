package com.linktic.reservation_system_api.util;

/**
 * Enum representing the various types of rooms available in the system.
 * These types define the classification of rooms based on their features.
 */
public enum RoomType {
    /**
     * Single room intended for one guest.
     */
    SINGLE,

    /**
     * Double room intended for two guests.
     */
    DOUBLE,

    /**
     * Suite room offering additional space and amenities.
     */
    SUITE,

    /**
     * Deluxe room providing enhanced features and comfort.
     */
    DELUXE
}
